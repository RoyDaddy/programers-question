SELECT b.TITLE,
       b.BOARD_ID,
       a.REPLY_ID,
       a.WRITER_ID,
       a.CONTENTS,
       TO_CHAR(a.CREATED_DATE, 'YYYY-MM-DD') as CREATED_DATE
FROM USED_GOODS_REPLY a
INNER JOIN USED_GOODS_BOARD b
ON a.BOARD_ID = b.BOARD_ID
WHERE TO_CHAR(b.CREATED_DATE, 'YYYYMM') = '202210'
ORDER BY a.CREATED_DATE asc, b.TITLE